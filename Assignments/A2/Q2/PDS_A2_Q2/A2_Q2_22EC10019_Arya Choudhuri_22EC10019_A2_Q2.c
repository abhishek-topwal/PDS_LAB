#include <stdio.h>

int main()	// main function
{
	int a;		//  variable to store positive integer
	char c;		//  variable to store character
	long double f;	//variable to store large floating point number
	scanf("%d %c %Lf",&a,&c,&f);
	printf("%2d \n",a);
	printf("%d \n",c);
	printf("%Le", f);
}

	


	
