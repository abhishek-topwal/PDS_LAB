#include<stdio.h>
#include<stdlib.h>
int main()
{
char b;
int a;
float c;
/*Printing A character*/
printf("Enter character\n");
scanf("%c",&b);
printf("Character=%c\n",b);
/*Printing a integer*/
printf("Enter integer\n");
scanf("%d",&a);
printf("Integer=%d\n",a);
/*Printing a Float value*/
printf("Enter a value\n");
scanf("%f",&c);
printf("Value=%f",c);
return 0;
}
