#include <stdio.h> 		// loads the standard library of pre-defined functions
int main()	   		// main() function denotes the start of code
{
	char c1, c2;		// Declares two variables for storing characters
	scanf("%c%c",&c1,&c2);	// Input values and stores in declared variables
	printf("%c%c",c1,c2);	// Displays the value stored in variables c1 & c2
	return 0;		// int main() function must return any integer
}
