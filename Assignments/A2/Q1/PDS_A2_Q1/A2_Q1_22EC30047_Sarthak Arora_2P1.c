/* this program reads two character using scanf and print the characters on the scree */
#include<stdio.h>
#include<stdlib.h>
int main()
{
	char c1,c2;  //two variables to store any character
	scanf("%c %c", &c1,&c2); //read any two variables
	printf("%c %c",c1,c2); //print the characters
	return 0;
}
