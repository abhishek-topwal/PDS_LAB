/* This program reads two characters using scanf amd print the characters on the screen. */
#include <stdio.h>
int main()
{
    char c1, c2;  //Two variable to store any character 
    scanf("%c%c" , &c1 , &c2 ); // Read any two characters
    printf("%c%c", c1, c2 ); // Print any two characters
    return 0 ; 
}

